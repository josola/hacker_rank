#include <iostream>

using std::cin;
using std::cout;
using std::endl;

int main()
{
	// Get numbers to sum.

	int num_a = 0, num_b = 0;

	cin >> num_a >> num_b;

	// Sum the numbers.

	int sum = num_a + num_b;

	// Finishing things up.

	cout << sum << endl;

	return 0;
}
